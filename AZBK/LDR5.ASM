	;
	; AZBK MEMORY LAYOUT:
	; 511. - BLITTER COMMANDS (SPR)			(1)
	; 512. ... 515.	- 256X256X4COLORS SCREEN	(4)
	; 516. ... 519.	- PROGRAM EXECUTIVE		(4)
	; 520. - OFFSCREEN FOR WALL/FLOOR/CEILING	(1)
	; 521. - OFFSCREEN FOR MONSTERS			(1)
	; 522. - CEIL IMAGE				(1)
	; 523. ... 540.	- 128X192X256COLORSX3LAYERS	(18)
	; 541. ... 572.	- TEXTURE DATA FOR RENDER	(32)
	; 2000.	... - TEMP.DATA, UNROLLED CODE,	ETC.	(X)
	;
	JMP START
	;
OLD34:	.WORD 0,0
OLD4:	.WORD 0,0
OLD100:	.WORD 0,0
OLD60:	.WORD 0,0
OLD274:	.WORD 0,0
AZWNDS:	.BLKW 22.		; SAVED	WINDOWS	REGS
AZVIDR:	.BLKW 10.		; SAVED	VIDEO REGS
AZPAL:	.BLKW 338.		; SAVED	PALLETES
PAL4:	.WORD 0,1037,1760,77777	; PALLETE FOR MENU
	;
INE:	.BYTE 233,232,224,236,221,0
TX1:	.ASCII/STARTUP.../<12><0>
TX2:	.ASCII/AZBK.../<0>
TX3:	.ASCII/NOT FOUND/<12><0>
TX4:	.ASCII/OK/<12><0>
TX5:	.ASCII/MACHINE.../<0>
TX6:	.ASCII/BK0010(-01)/<12><0>
TX7:	.ASCII/BK0011M/<12><0>
TX8:	.ASCII/LOADING.../<0>
TX9:	.ASCII/ERROR/<12><0>
	.EVEN
	;
START:	CALL DETBK		; DETECT BK10/BK11
	EMT 14			; TODO:	REMOVE
	MOV #INE,R1
	CLR R2
	EMT 20
	MOV #214,R0
	EMT 16
	MOV #TX1,R1
	CLR R2
	EMT 20
	MOV #TX2,R1
	CLR R2
	EMT 20
	CALL DETAZ		; DETECT AZBK
	BEQ 1$
	MOV #TX3,R1
	CLR R2
	EMT 20
	EMT 6
	MOV #214,R0
	EMT 16
	EMT 14
	RET			; TODO:	NO AZBK	FOUND
1$:	MOV #TX4,R1
	CLR R2
	EMT 20
	MOV #TX5,R1
	CLR R2
	EMT 20
	;
	MOV #OLD34,R0
	MOV @#34,(R0)+
	MOV @#36,(R0)+
	MOV @#4,(R0)+
	MOV @#6,(R0)+
	MOV @#100,(R0)+
	MOV @#102,(R0)+
	MOV @#60,(R0)+
	MOV @#62,(R0)+
	MOV @#274,(R0)+
	MOV @#276,(R0)+
	;
	MOV #TRAP,@#34
	MOV #200,@#36
	;
	; SAVE AZBK WINDOWS REGS
	MOV #22.,R0
	SUB SYS,R0		; 21. IF BK0010
	MOV #177300,R1
	MOV #AZWNDS,R2
	MOV (R1)+,(R2)+
	SOB R0,.-2
	;
	CLR R2
	TST SYS
	BNE 2$
	MOV #TX7,R1		; BK11M
	BR 20$
2$:	MOV #TX6,R1
20$:	EMT 20
	MOV #TX8,R1		; BK10
	CLR R2
	EMT 20
	;
	CALL ILOAD
	BEQ 23$
	MOV #TX9,R1
	CLR R2
	EMT 20
	EMT 6
	MOV #214,R0
	EMT 16
	JMP FAIL
	;
23$:	MOV #TX4,R1
	CLR R2
	EMT 20
	;
	SOB R2,.		; LITTLE PAUSE
	SOB R2,.
	;
	MOV #14,R0
	EMT 16
	MOV #214,R0
	EMT 16
	;
	; SAVE AZBK VIDEO REGS
	MOV #AZVIDR,R2
	MOV @#177230,(R2)+
	MOV @#177232,(R2)+
	MOV #8.,R0
	MOV #177240,R1
	MOV (R1)+,(R2)+
	SOB R0,.-2
	;
	; SAVE AZBK PALLETES
	MOV #338.,R0
	CLR R1
	MOV #AZPAL,R2
3$:	MOV R1,@#177234
	MOV @#177236,(R2)+
	INC R1
	SOB R0,3$
	;
	; BK11M	MEMORY CONFIGURE
	TST SYS
	BNE 21$
	MOV #14020,@#177716	; ROM IN 100000-140000
	MOV #40000,@#177662	;
	;
	; SET MEMORY PAGES LAYOUT
21$:	MOV #177310,R0		; START	WINDOW
	MOV #512.,R1		; START	BLOCK NUMBER
	MOV #8.,R2		; 1ST 8	WINDOWS
	MOV R1,(R0)+
	INC R1
	SOB R2,.-4
	MOV #520.,@#177334	; MAKE DUMMY
	;
	; SET MEMORY MODE
	MOV #177340,R0
	MOV #177400,R1
	BIS R1,(R0)+
	BIC R1,(R0)+
	BIC R1,(R0)+
	;
	; CLEAR	MEMORY
	MOV #31.,R0		; NUMBER OF PAGES
	MOV #511.,R1		; START	WORKING	PAGE
4$:	CMP #520.,R1		; EXCLUDE 520. PAGE
	BEQ 44$			;
	CMP #522.,R1		; EXCLUDE 522. PAGE
	BEQ 44$			;
	MOV R1,@#177334		; MAP TO 160000
	MOV #2048.,R2
	MOV #160000,R3
	CLR (R3)+
	SOB R2,.-2
44$:	INC R1
	SOB R0,4$
	;
	; SETUP	MENU PALLETE
	MOV #4,R0
	MOV #256.,R1
	MOV #PAL4,R2
5$:	MOV R1,@#177234
	INC R1
	MOV (R2)+,@#177236
	SOB R0,5$
	;
	CALL MKPAL	; MAKE GAME PALLETE
	CALL MKTXT	; MAKE TEXTURES	DATA
	CALL MKMRR	; MAKE MIRROR DATA
	;
	; RUN GAME
	MOV #DATA,R0
	MOV #100000,R1
	MOV #20000,R2	; GAME LENGTH IN WORDS
	MOV (R0)+,(R1)+
	SOB R2,.-2
	MOV SP,(PC)+
OLDSP:	.WORD 0
	MOV #131724,SP
	CALL @#100000
	MOV OLDSP,SP
	;
	; RESTORE AZBK PALLETES
	MOV #338.,R0
	CLR R1
	MOV #AZPAL,R2
1$:	MOV R1,@#177234
	MOV (R2)+,@#177236
	INC R1
	SOB R0,1$
	;
	; RESTORE AZBK VIDEO REGS
	MOV #AZVIDR,R2
	MOV (R2)+,@#177230
	MOV (R2)+,@#177232
	MOV #8.,R0
	MOV #177240,R1
	MOV (R2)+,(R1)+
	SOB R0,.-2
	;
	; RESTORE AZBK WINDOWS REGS
FAIL:	MOV #19.,R0
	MOV #177300,R1
	MOV #AZWNDS,R2
	MOV (R2)+,(R1)+
	SOB R0,.-2
	CMP (R2)+,(R1)+		; SKIP 177346
	MOV (R2)+,@#177130	; RESTORE SMK MODE
	TST SYS
	BNE 2$
	MOV (R2)+,@#177716	; RESTORE 177716 (BK11M)
	;
2$:	MOV #OLD34,R0
	MOV (R0)+,@#34
	MOV (R0)+,@#36
	MOV (R0)+,@#4
	MOV (R0)+,@#6
	MOV (R0)+,@#100
	MOV (R0)+,@#102
	MOV (R0)+,@#60
	MOV (R0)+,@#62
	MOV (R0)+,@#274
	MOV (R0)+,@#276
	;
	JMP @#100000		; EXIT INTO DOS
	;
	; INTERNAL DATA	LOADER
	; RETURN ZERO IF OK OR NON ZERO	IF FAILED
ILOAD:	MOV #9.,R4		; TEXTURES COUNT
	MOVB #'F,TXFN+7		; LOAD FONT
	MOV #EMT36P,R1
	EMT 36
	TSTB EMT36P+1
	BNE 2$
	CALL CPYFNT
	MOVB #'C,TXFN+7		; LOAD CEIL IMAGE
	MOV #EMT36P,R1
	EMT 36
	TSTB EMT36P+1
	BNE 2$
	CALL CPYCEL
	MOVB #'1,TXFN+7		; LOAD TEXTURES
	MOV #2000.,R5		; START AZBK PAGE
1$:	MOV #EMT36P,R1
	EMT 36
	TSTB EMT36P+1
	BNE 2$
	MOV #40000,R1
	CALL EMTCPY
	CALL EMTCPY
	INCB TXFN+7
	SOB R4,1$
	CALL CLIBUF
2$:	TST R4
	RET
	;
CPYCEL:	MOV #522.,R5		; CEIL IMG. AZBK PAGE
	MOV #40000,R1
	CALL EMTCPY
	MOV #520.,R5
	MOV #40000,R1
	;
EMTCPY:	MOV R5,@#177334
	INC R5
	MOV #40000,R0
	MOV #177340,R2
	BIS R0,(R2)+
	BIC R0,(R2)+
	BIC R0,(R2)+
	MOV #2048.,R0		; LEN. IN WORDS!
	MOV #160000,R2
	MOV (R1)+,(R2)+
	SOB R0,.-2
	MOV AZWNDS+34,@#177334
	MOV AZWNDS+40,@#177340
	MOV AZWNDS+42,@#177342
	MOV AZWNDS+44,@#177344
	RET
	;
EMT36P:	.BYTE 3
	.BYTE 0
	.WORD 40000
	.WORD 0
TXFN:	.ASCII/BK3DRES1/<20><20><20><20><20><20><20><20>
	.WORD 0
	.WORD 0
	.BLKB 16.
	.EVEN
	;
CLIBUF:	MOV #4096.,R0
	MOV #40000,R1
	CLR (R1)+
	SOB R0,.-2
	RET
	;
	; DETECT BK TYPE 10/11
DETBK:	CLR (PC)+	; 0=BK11
SYS:	.WORD 0
	CMPB @#177717,#300
	BEQ 1$
	INC SYS		; 1=BK10
1$:	RET
	;
	; DETECT AZBK CONTROLLER
DETAZ:	MOV @#4,-(SP)
	MOV #I4,@#4
	CLR FL
	TST @#177220
	MOV (SP)+,@#4
	TST FL
	RET
I4:	INC (PC)+
FL:	.WORD 0
	RTI
	;
	; COPY FONT INTO END OF	RAM
CPYFNT:	MOV #40000,R0
	MOV #34200,R1
	MOV #1700,R2
	MOV (R0)+,(R1)+
	SOB R2,.-2
	RET
	;
	; MAKE GAME PALLETE
MKPAL:	MOV @#177334,-(SP)
	MOV #2016.,@#177334	; LOADED PALLETE
	MOV #256.,R0
	CLR R1
	MOV #160000,R2
1$:	MOV R1,@#177234
	INC R1
	MOV (R2)+,@#177236
	SOB R0,1$
	MOV (SP)+,@#177334
	RET
	;
	; MAKE TEXTURES	DATA
	; SRC.PGS.:2000.,2001.,...
	; DST.PGS.:541.,542.,...
MKTXT:	MOV @#177320,-(SP)
	MOV @#177322,-(SP)	; 100000-120000
	MOV @#177334,-(SP)	; 160000-170000
	MOV #572.,R3		; 572.-541. (REVERSE)
	MOV #32.,R0
1$:	MOV R3,@#177334
	DEC R3
	MOV #8.,R1
	MOV #2000.,R2
2$:	MOV R2,@#177320
	INC R2
	MOV R2,@#177322
	INC R2
	MOV R0,-(SP)
	MOV R1,-(SP)
	MOV R2,-(SP)
	MOV R3,-(SP)
	DEC R0
	ASL R0
	ASL R0
	ASL R0
	ASL R0
	ADD #100000,R0
	MOV #8.,-(SP)
	SUB R1,(SP)
	MOV (SP)+,R3
	SWAB R3
	ASL R3
	ADD #160000,R3
	MOV #16.,R1
4$:	MOV #16.,R2
3$:	MOVB (R0),(R3)+
	MOVB (R0),(R3)+
	ADD #512.,R0
	SOB R2,3$
	SUB #8192.-1.,R0
	SOB R1,4$
	MOV (SP)+,R3
	MOV (SP)+,R2
	MOV (SP)+,R1
	MOV (SP)+,R0
	SOB R1,2$
	SOB R0,1$
	MOV (SP)+,@#177334
	MOV (SP)+,@#177322
	MOV (SP)+,@#177320
	MOV #541.,@#177336	; SET DEFAULT
	RET
	;
	; MAKE MIRROR BLITTER COMMANDS SET
	;
	; HELP SUBROUTINES
CPYW:	MOV #10020,(R0)+	; 1 MSBS (24_BIT_ADDR.FOLLOWS)
	MOV R1,(R0)+		; 2 LSBS SRC
	MOV R2,(R0)+		; 3 LSBS DST
	MOV #11,(R0)+		; 4 COPY SRC->DST
	MOV #20077,(R0)+	; 5 LEN=64.-1.ROWS=1.
	MOV #101,(R0)+		; 6 =101
	CLR (R0)+		; 7 NOT	USED
	CLR (R0)+		; 8 NOT	USED
	ADD #64.,R2
	RET
	;
MKMRR:	MOV #511.,@#177334	; MAP TO 160000
	MOV #160000,R0		; READY	TO WRITE INTO
	; COPY WALLS
	MOV #40000,R1		; LSBS SOURCE
	MOV #140000,R2		; LSBS DESTINATION
	CALL CPYW
	CALL CPYW		; 2 AZBK COMMANDS
	; MIRROR WALLS
	MOV #140000,R1
	MOV #157600,R2
	MOV #32.,R3
1$:	MOV #10020,(R0)+
	MOV R1,(R0)+
	MOV R2,(R0)+
	MOV #11,(R0)+
	MOV #1077,(R0)+
	MOV #1,(R0)+
	CLR (R0)+
	CLR (R0)+
	ADD #128.,R1
	SUB #128.,R2
	SOB R3,1$		; 2.+32.=34. COMMANDS
	; COPY MONSTERS
	MOV #44000,R1
	MOV #120000,R2
	CALL CPYW
	CALL CPYW
	; CLEAR	MONSTERS BUFFER
	MOV #20,(R0)+
	CLR (R0)+
	MOV R1,(R0)+
	CLR (R0)+
	MOV #20077,(R0)+
	MOV #1,(R0)+
	CLR (R0)+
	CLR (R0)+		; 34.+3.=37. COMMANDS
	; COPY CEIL IMG. INTO WALLS OFFSCREEN
	MOV #10020,(R0)+	;
	MOV #50000,(R0)+	;
	MOV #40000,(R0)+	;
	MOV #11,(R0)+		;
	MOV #20077,(R0)+	;
	MOV #1,(R0)+		;
	CLR (R0)+
	CLR (R0)+		; 34.+3.+1.=38. COMMANDS
	;
	RET
	;
	; TRAP HANDLER
	; TRAP 0 - MENU	VIDEO MODE
	; TRAP 1 - GAME	VIDEO MODE
	; TRAP 2 - SET WINDOW IN 160000	(R1=WINDOW)
TRAP:	MOV R0,-(SP)
	MOV 2(SP),R0
	MOV -(R0),R0
	BIC #177400,R0
	ASL R0
	ADD R0,PC
	BR TR0
	BR TR1
	BR TR2
TREXIT:	MOV (SP)+,R0
	RTI
	;
TR0:	MOV #512.,@#177232
	MOV #512.,@#177240
	MOV #512.,@#177242
	MOV #12201,@#177230
	BR TREXIT
	;
TR1:	MOV #523.,@#177232
	MOV #529.,@#177240
	MOV #535.,@#177242
	MOV #23317,@#177230
	BR TREXIT
	;
TR2:	MOV R1,@#177334
	BR TREXIT
	;
	.END
